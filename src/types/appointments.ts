export const TIME_SLOTS = [
  '08:00', '09:00', '09:30', '10:00', '10:30', '11:00',
  '12:30', '13:00', '13:30', '14:00', '14:30',
  '15:00', '15:30', 'Work In'
] as const;

export type TimeSlot = typeof TIME_SLOTS[number];

export interface Appointment {
  id: number;
  date: string;
  time: TimeSlot;
  salesOrder: string;
  delivery: string;
  carrier?: string;
  notes?: string;
  source: 'manual' | 'upload';
  createdAt?: string;
  updatedAt?: string;
}

export interface AppointmentInput {
  date: string;
  time: string;
  salesOrder: string;
  delivery: string;
  carrier?: string;
  notes?: string;
  source?: 'manual' | 'upload';
}
